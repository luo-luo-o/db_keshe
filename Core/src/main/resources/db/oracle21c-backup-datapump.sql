/*
  PSM-Smart Oracle 21c Data Pump backup extension.

  Replaces the previous in-schema DATA_SNAPSHOT_* copy tables with table-mode
  DBMS_DATAPUMP jobs. Only business table rows are exported/imported.
*/

BEGIN
  EXECUTE IMMEDIATE 'DROP PACKAGE PKG_PSM_BACKUP';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -4043 THEN RAISE; END IF;
END;
/

