CREATE OR REPLACE PACKAGE BODY PKG_PSM_TASK AS
  PROCEDURE UPDATE_TASK(
      P_TASK_ID   IN NUMBER,
      P_STATUS    IN NUMBER,
      P_ASSIGNEE  IN VARCHAR2,
      P_FEEDBACK  IN VARCHAR2,
      P_ACTOR     IN VARCHAR2
  ) IS
  BEGIN
    IF P_STATUS NOT IN (0, 1, 2) THEN
      RAISE_APPLICATION_ERROR(-20001, 'Invalid task status.');
    END IF;

    UPDATE MAINT_TASK
    SET
      STATUS = P_STATUS,
      ASSIGNEE = NULLIF(TRIM(P_ASSIGNEE), ''),
      FEEDBACK = NULLIF(TRIM(P_FEEDBACK), ''),
      UPDATED_AT = LOCALTIMESTAMP,
      FINISHED_AT = CASE
        WHEN P_STATUS = 2 THEN NVL(FINISHED_AT, LOCALTIMESTAMP)
        ELSE NULL
      END
    WHERE TASK_ID = P_TASK_ID;

    IF SQL%ROWCOUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20002, 'Task does not exist.');
    END IF;

    COMMIT;
    PKG_PSM_LOG.WRITE_LOG(
      'INFO',
      'TASK_UPDATE',
      'Updated maintenance task ' || P_TASK_ID,
      'status=' || P_STATUS || ' actor=' || NVL(P_ACTOR, 'unknown')
    );
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      PKG_PSM_LOG.WRITE_LOG('ERROR', 'TASK_UPDATE', SQLERRM, 'taskId=' || P_TASK_ID);
      RAISE;
  END UPDATE_TASK;
END PKG_PSM_TASK;
