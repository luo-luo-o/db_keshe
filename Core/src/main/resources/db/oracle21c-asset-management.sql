END PKG_PSM_BACKUP;
/

CREATE OR REPLACE PACKAGE BODY PKG_PSM_BACKUP AS
  FUNCTION NORMALIZE_TEXT(P_TEXT IN VARCHAR2, P_LIMIT IN NUMBER) RETURN VARCHAR2 IS
  BEGIN
    RETURN SUBSTR(NULLIF(TRIM(P_TEXT), ''), 1, P_LIMIT);
  END NORMALIZE_TEXT;

  PROCEDURE ASSERT_SNAPSHOT(P_SNAPSHOT_ID IN NUMBER) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM DATA_SNAPSHOT
    WHERE ID = P_SNAPSHOT_ID;

    IF V_COUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20600, 'Snapshot does not exist.');
    END IF;
  END ASSERT_SNAPSHOT;

  PROCEDURE BUMP_SEQUENCE(P_SEQUENCE_NAME IN VARCHAR2, P_MIN_NEXT_VALUE IN NUMBER) IS
    V_CURRENT_VALUE NUMBER;
    V_INCREMENT NUMBER;
  BEGIN
    IF P_MIN_NEXT_VALUE IS NULL OR P_MIN_NEXT_VALUE <= 1 THEN
      RETURN;
    END IF;

    EXECUTE IMMEDIATE 'SELECT ' || P_SEQUENCE_NAME || '.NEXTVAL FROM DUAL' INTO V_CURRENT_VALUE;
    IF V_CURRENT_VALUE < P_MIN_NEXT_VALUE THEN
      V_INCREMENT := P_MIN_NEXT_VALUE - V_CURRENT_VALUE;
      EXECUTE IMMEDIATE 'ALTER SEQUENCE ' || P_SEQUENCE_NAME || ' INCREMENT BY ' || V_INCREMENT;
      EXECUTE IMMEDIATE 'SELECT ' || P_SEQUENCE_NAME || '.NEXTVAL FROM DUAL' INTO V_CURRENT_VALUE;
      EXECUTE IMMEDIATE 'ALTER SEQUENCE ' || P_SEQUENCE_NAME || ' INCREMENT BY 1';
    END IF;
  EXCEPTION
    WHEN OTHERS THEN
      BEGIN
        EXECUTE IMMEDIATE 'ALTER SEQUENCE ' || P_SEQUENCE_NAME || ' INCREMENT BY 1';
      EXCEPTION
        WHEN OTHERS THEN NULL;
      END;
      RAISE;
  END BUMP_SEQUENCE;

  PROCEDURE BUMP_ALL_SEQUENCES IS
    V_MIN_NEXT_VALUE NUMBER;
  BEGIN
    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM BOX_TRANSFORMER;
    BUMP_SEQUENCE('SEQ_BOX_TRANSFORMER', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM POWER_CIRCUIT;
    BUMP_SEQUENCE('SEQ_POWER_CIRCUIT', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM MEASURE_POINT;
    BUMP_SEQUENCE('SEQ_MEASURE_POINT', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM TS_RAW_DATA;
    BUMP_SEQUENCE('SEQ_TS_RAW_DATA', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM TS_RAW_DATA_DAY_ARCHIVE;
    BUMP_SEQUENCE('SEQ_TS_RAW_DATA_DAY_ARCHIVE', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM CABINET_DOOR_LOG;
    BUMP_SEQUENCE('SEQ_CABINET_DOOR_LOG', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM ALARM_LOG;
    BUMP_SEQUENCE('SEQ_ALARM_LOG', V_MIN_NEXT_VALUE);

    SELECT NVL(MAX(TASK_ID), 0) + 1 INTO V_MIN_NEXT_VALUE FROM MAINT_TASK;
    BUMP_SEQUENCE('SEQ_MAINT_TASK', V_MIN_NEXT_VALUE);
  END BUMP_ALL_SEQUENCES;

  PROCEDURE CREATE_SNAPSHOT(
      P_SNAPSHOT_NAME IN VARCHAR2,
      P_NOTE          IN VARCHAR2,
      P_ACTOR         IN VARCHAR2,
      P_SNAPSHOT_ID   OUT NUMBER
  ) IS
    V_NAME VARCHAR2(100) := NVL(NORMALIZE_TEXT(P_SNAPSHOT_NAME, 100), 'snapshot-' || TO_CHAR(SYSDATE, 'YYYYMMDDHH24MISS'));
    V_NOTE VARCHAR2(500) := NORMALIZE_TEXT(P_NOTE, 500);
    V_ACTOR VARCHAR2(64) := NORMALIZE_TEXT(P_ACTOR, 64);
    V_TRANSFORMER_COUNT NUMBER := 0;
    V_CIRCUIT_COUNT NUMBER := 0;
    V_POINT_COUNT NUMBER := 0;
    V_RAW_DATA_COUNT NUMBER := 0;
    V_ARCHIVE_COUNT NUMBER := 0;
    V_DOOR_LOG_COUNT NUMBER := 0;
    V_ALARM_COUNT NUMBER := 0;
    V_TASK_COUNT NUMBER := 0;
  BEGIN
    P_SNAPSHOT_ID := SEQ_DATA_SNAPSHOT.NEXTVAL;

    INSERT INTO DATA_SNAPSHOT (
      ID,
      SNAPSHOT_NAME,
      NOTE,
      CREATED_BY,
      CREATED_AT
    ) VALUES (
      P_SNAPSHOT_ID,
      V_NAME,
      V_NOTE,
      V_ACTOR,
      LOCALTIMESTAMP
    );

    INSERT INTO DATA_SNAPSHOT_TRANSFORMER (
      SNAPSHOT_ID, ID, TRANSFORMER_CODE, NAME, TRANSFORMER_TYPE,
      RATED_CAPACITY_KVA, RATED_VOLTAGE_RATIO, COMMISSION_DATE, MANUFACTURER,
      OIL_LEVEL, LOCATION, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, TRANSFORMER_CODE, NAME, TRANSFORMER_TYPE,
      RATED_CAPACITY_KVA, RATED_VOLTAGE_RATIO, COMMISSION_DATE, MANUFACTURER,
      OIL_LEVEL, LOCATION, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    FROM BOX_TRANSFORMER;
    V_TRANSFORMER_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_CIRCUIT (
      SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_CODE, NAME, DIRECTION,
      RATED_VOLTAGE_KV, RATED_CURRENT_A, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_CODE, NAME, DIRECTION,
      RATED_VOLTAGE_KV, RATED_CURRENT_A, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    FROM POWER_CIRCUIT;
    V_CIRCUIT_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_POINT (
      SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_CODE, POINT_NAME,
      POINT_GROUP, MEASURE_TYPE, PHASE_CODE, UNIT, MIN_LIMIT, MAX_LIMIT,
      RATE_LIMIT, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_CODE, POINT_NAME,
      POINT_GROUP, MEASURE_TYPE, PHASE_CODE, UNIT, MIN_LIMIT, MAX_LIMIT,
      RATE_LIMIT, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    FROM MEASURE_POINT;
    V_POINT_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_RAW_DATA (
      SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, SAMPLE_TIME,
      VAL, QUALITY_FLAG, CREATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, SAMPLE_TIME,
      VAL, QUALITY_FLAG, CREATED_AT
    FROM TS_RAW_DATA;
    V_RAW_DATA_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_DAY_ARCHIVE (
      SNAPSHOT_ID, ID, ARCHIVE_DAY, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID,
      DISPLAY_VAL, AVG_VAL, MIN_VAL, MAX_VAL, SAMPLE_COUNT, MAX_QUALITY_FLAG,
      CREATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, ARCHIVE_DAY, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID,
      DISPLAY_VAL, AVG_VAL, MIN_VAL, MAX_VAL, SAMPLE_COUNT, MAX_QUALITY_FLAG,
      CREATED_AT
    FROM TS_RAW_DATA_DAY_ARCHIVE;
    V_ARCHIVE_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_DOOR_LOG (
      SNAPSHOT_ID, ID, TRANSFORMER_ID, POINT_ID, EVENT_TYPE, EVENT_TIME,
      DOOR_STATUS, NOTE, CREATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, TRANSFORMER_ID, POINT_ID, EVENT_TYPE, EVENT_TIME,
      DOOR_STATUS, NOTE, CREATED_AT
    FROM CABINET_DOOR_LOG;
    V_DOOR_LOG_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_ALARM (
      SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, ALARM_TYPE,
      ALARM_LEVEL, START_TIME, END_TIME, START_VAL, END_VAL, STATUS, CREATED_AT
    )
    SELECT
      P_SNAPSHOT_ID, ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, ALARM_TYPE,
      ALARM_LEVEL, START_TIME, END_TIME, START_VAL, END_VAL, STATUS, CREATED_AT
    FROM ALARM_LOG;
    V_ALARM_COUNT := SQL%ROWCOUNT;

    INSERT INTO DATA_SNAPSHOT_TASK (
      SNAPSHOT_ID, TASK_ID, ALARM_ID, STATUS, ASSIGNEE, FEEDBACK,
      CREATED_AT, UPDATED_AT, FINISHED_AT
    )
    SELECT
      P_SNAPSHOT_ID, TASK_ID, ALARM_ID, STATUS, ASSIGNEE, FEEDBACK,
      CREATED_AT, UPDATED_AT, FINISHED_AT
    FROM MAINT_TASK;
    V_TASK_COUNT := SQL%ROWCOUNT;

    UPDATE DATA_SNAPSHOT
    SET
      TRANSFORMER_COUNT = V_TRANSFORMER_COUNT,
      CIRCUIT_COUNT = V_CIRCUIT_COUNT,
      POINT_COUNT = V_POINT_COUNT,
      RAW_DATA_COUNT = V_RAW_DATA_COUNT,
      ARCHIVE_COUNT = V_ARCHIVE_COUNT,
      DOOR_LOG_COUNT = V_DOOR_LOG_COUNT,
      ALARM_COUNT = V_ALARM_COUNT,
      TASK_COUNT = V_TASK_COUNT
    WHERE ID = P_SNAPSHOT_ID;

    COMMIT;
    PKG_PSM_LOG.WRITE_LOG(
      'INFO',
      'BACKUP_CREATE',
      'Created data snapshot ' || P_SNAPSHOT_ID,
      'actor=' || NVL(V_ACTOR, 'unknown') || ' rawRows=' || V_RAW_DATA_COUNT
    );
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      PKG_PSM_LOG.WRITE_LOG('ERROR', 'BACKUP_CREATE', SQLERRM, DBMS_UTILITY.FORMAT_ERROR_BACKTRACE);
      RAISE;
  END CREATE_SNAPSHOT;

  PROCEDURE RESTORE_SNAPSHOT(
      P_SNAPSHOT_ID IN NUMBER,
      P_ACTOR       IN VARCHAR2
  ) IS
    V_ACTOR VARCHAR2(64) := NORMALIZE_TEXT(P_ACTOR, 64);
  BEGIN
    ASSERT_SNAPSHOT(P_SNAPSHOT_ID);

    DELETE FROM MAINT_TASK;
    DELETE FROM ALARM_LOG;
    DELETE FROM CABINET_DOOR_LOG;
    DELETE FROM TS_RAW_DATA;
    DELETE FROM TS_RAW_DATA_DAY_ARCHIVE;
    DELETE FROM MEASURE_POINT;
    DELETE FROM POWER_CIRCUIT;
    DELETE FROM BOX_TRANSFORMER;

    INSERT INTO BOX_TRANSFORMER (
      ID, TRANSFORMER_CODE, NAME, TRANSFORMER_TYPE, RATED_CAPACITY_KVA,
      RATED_VOLTAGE_RATIO, COMMISSION_DATE, MANUFACTURER, OIL_LEVEL,
      LOCATION, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    )
    SELECT
      ID, TRANSFORMER_CODE, NAME, TRANSFORMER_TYPE, RATED_CAPACITY_KVA,
      RATED_VOLTAGE_RATIO, COMMISSION_DATE, MANUFACTURER, OIL_LEVEL,
      LOCATION, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    FROM DATA_SNAPSHOT_TRANSFORMER
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO POWER_CIRCUIT (
      ID, TRANSFORMER_ID, CIRCUIT_CODE, NAME, DIRECTION, RATED_VOLTAGE_KV,
      RATED_CURRENT_A, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    )
    SELECT
      ID, TRANSFORMER_ID, CIRCUIT_CODE, NAME, DIRECTION, RATED_VOLTAGE_KV,
      RATED_CURRENT_A, STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    FROM DATA_SNAPSHOT_CIRCUIT
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO MEASURE_POINT (
      ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_CODE, POINT_NAME, POINT_GROUP,
      MEASURE_TYPE, PHASE_CODE, UNIT, MIN_LIMIT, MAX_LIMIT, RATE_LIMIT,
      STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    )
    SELECT
      ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_CODE, POINT_NAME, POINT_GROUP,
      MEASURE_TYPE, PHASE_CODE, UNIT, MIN_LIMIT, MAX_LIMIT, RATE_LIMIT,
      STATUS, IS_DELETED, CREATED_AT, UPDATED_AT
    FROM DATA_SNAPSHOT_POINT
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO TS_RAW_DATA (
      ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, SAMPLE_TIME, VAL,
      QUALITY_FLAG, CREATED_AT
    )
    SELECT
      ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, SAMPLE_TIME, VAL,
      QUALITY_FLAG, CREATED_AT
    FROM DATA_SNAPSHOT_RAW_DATA
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO TS_RAW_DATA_DAY_ARCHIVE (
      ID, ARCHIVE_DAY, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, DISPLAY_VAL,
      AVG_VAL, MIN_VAL, MAX_VAL, SAMPLE_COUNT, MAX_QUALITY_FLAG, CREATED_AT
    )
    SELECT
      ID, ARCHIVE_DAY, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, DISPLAY_VAL,
      AVG_VAL, MIN_VAL, MAX_VAL, SAMPLE_COUNT, MAX_QUALITY_FLAG, CREATED_AT
    FROM DATA_SNAPSHOT_DAY_ARCHIVE
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO CABINET_DOOR_LOG (
      ID, TRANSFORMER_ID, POINT_ID, EVENT_TYPE, EVENT_TIME, DOOR_STATUS,
      NOTE, CREATED_AT
    )
    SELECT
      ID, TRANSFORMER_ID, POINT_ID, EVENT_TYPE, EVENT_TIME, DOOR_STATUS,
      NOTE, CREATED_AT
    FROM DATA_SNAPSHOT_DOOR_LOG
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO ALARM_LOG (
      ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, ALARM_TYPE, ALARM_LEVEL,
      START_TIME, END_TIME, START_VAL, END_VAL, STATUS, CREATED_AT
    )
    SELECT
      ID, TRANSFORMER_ID, CIRCUIT_ID, POINT_ID, ALARM_TYPE, ALARM_LEVEL,
      START_TIME, END_TIME, START_VAL, END_VAL, STATUS, CREATED_AT
    FROM DATA_SNAPSHOT_ALARM
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    INSERT INTO MAINT_TASK (
      TASK_ID, ALARM_ID, STATUS, ASSIGNEE, FEEDBACK, CREATED_AT,
      UPDATED_AT, FINISHED_AT
    )
    SELECT
      TASK_ID, ALARM_ID, STATUS, ASSIGNEE, FEEDBACK, CREATED_AT,
      UPDATED_AT, FINISHED_AT
    FROM DATA_SNAPSHOT_TASK
    WHERE SNAPSHOT_ID = P_SNAPSHOT_ID;

    UPDATE BOX_TRANSFORMER BT
    SET (STATUS, IS_DELETED, UPDATED_AT) = (
      SELECT S.STATUS, S.IS_DELETED, S.UPDATED_AT
      FROM DATA_SNAPSHOT_TRANSFORMER S
      WHERE S.SNAPSHOT_ID = P_SNAPSHOT_ID
        AND S.ID = BT.ID
    )
    WHERE EXISTS (
      SELECT 1
      FROM DATA_SNAPSHOT_TRANSFORMER S
      WHERE S.SNAPSHOT_ID = P_SNAPSHOT_ID
        AND S.ID = BT.ID
    );

    UPDATE POWER_CIRCUIT PC
    SET (STATUS, IS_DELETED, UPDATED_AT) = (
      SELECT S.STATUS, S.IS_DELETED, S.UPDATED_AT
      FROM DATA_SNAPSHOT_CIRCUIT S
      WHERE S.SNAPSHOT_ID = P_SNAPSHOT_ID
        AND S.ID = PC.ID
    )
    WHERE EXISTS (
      SELECT 1
      FROM DATA_SNAPSHOT_CIRCUIT S
      WHERE S.SNAPSHOT_ID = P_SNAPSHOT_ID
        AND S.ID = PC.ID
    );

    UPDATE SIM_CONTROL
    SET
      RUNNING = 0,
      ANOMALY_ENABLED = 0,
      STARTED_AT = NULL,
      LAST_WRITE_AT = (SELECT MAX(SAMPLE_TIME) FROM TS_RAW_DATA),
      WRITE_COUNT = (SELECT COUNT(1) FROM TS_RAW_DATA),
      ALARM_COUNT = (SELECT COUNT(1) FROM ALARM_LOG),
      TASK_COUNT = (SELECT COUNT(1) FROM MAINT_TASK),
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = 1;

    UPDATE DATA_SNAPSHOT
    SET
      RESTORED_BY = V_ACTOR,
      RESTORED_AT = LOCALTIMESTAMP
    WHERE ID = P_SNAPSHOT_ID;

    BUMP_ALL_SEQUENCES;

    COMMIT;
    PKG_PSM_LOG.WRITE_LOG(
      'INFO',
      'BACKUP_RESTORE',
      'Restored data snapshot ' || P_SNAPSHOT_ID,
      'actor=' || NVL(V_ACTOR, 'unknown')
    );
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      PKG_PSM_LOG.WRITE_LOG('ERROR', 'BACKUP_RESTORE', SQLERRM, DBMS_UTILITY.FORMAT_ERROR_BACKTRACE);
      RAISE;
  END RESTORE_SNAPSHOT;
END PKG_PSM_BACKUP;
