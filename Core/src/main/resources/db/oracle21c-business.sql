CREATE OR REPLACE PACKAGE PKG_PSM_TASK AS
  PROCEDURE UPDATE_TASK(
      P_TASK_ID   IN NUMBER,
      P_STATUS    IN NUMBER,
      P_ASSIGNEE  IN VARCHAR2,
      P_FEEDBACK  IN VARCHAR2,
      P_ACTOR     IN VARCHAR2
  );
END PKG_PSM_TASK;
