  V_COUNT NUMBER;
BEGIN
  SELECT COUNT(1) INTO V_COUNT FROM USER_SEQUENCES WHERE SEQUENCE_NAME = 'SEQ_DATA_SNAPSHOT';
  IF V_COUNT = 0 THEN
    EXECUTE IMMEDIATE 'CREATE SEQUENCE SEQ_DATA_SNAPSHOT START WITH 1 INCREMENT BY 1 NOCACHE';
  END IF;
END;
