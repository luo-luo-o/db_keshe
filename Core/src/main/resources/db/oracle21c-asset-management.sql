CREATE OR REPLACE TRIGGER TRG_PSM_TASK_COMPLETE_STATUS
AFTER UPDATE OF STATUS ON MAINT_TASK
FOR EACH ROW
DECLARE
  V_TRANSFORMER_ID NUMBER;
  V_CIRCUIT_ID NUMBER;
  V_ACTIVE_COUNT NUMBER;
BEGIN
  IF :NEW.STATUS = 2 AND NVL(:OLD.STATUS, -1) != 2 THEN
    SELECT TRANSFORMER_ID, CIRCUIT_ID
    INTO V_TRANSFORMER_ID, V_CIRCUIT_ID
    FROM ALARM_LOG
    WHERE ID = :NEW.ALARM_ID;

    IF V_CIRCUIT_ID IS NOT NULL THEN
      SELECT COUNT(1)
      INTO V_ACTIVE_COUNT
      FROM ALARM_LOG A
      LEFT JOIN MEASURE_POINT MP ON MP.ID = A.POINT_ID
      WHERE A.CIRCUIT_ID = V_CIRCUIT_ID
        AND A.STATUS = 0
        AND (A.POINT_ID IS NULL OR NVL(MP.IS_DELETED, 0) = 0);

      IF V_ACTIVE_COUNT = 0 THEN
        UPDATE POWER_CIRCUIT
        SET STATUS = 0,
            UPDATED_AT = LOCALTIMESTAMP
        WHERE ID = V_CIRCUIT_ID
          AND IS_DELETED = 0
          AND STATUS = 1;
      END IF;
    END IF;

    SELECT COUNT(1)
    INTO V_ACTIVE_COUNT
    FROM ALARM_LOG A
    LEFT JOIN POWER_CIRCUIT PC ON PC.ID = A.CIRCUIT_ID
    LEFT JOIN MEASURE_POINT MP ON MP.ID = A.POINT_ID
    WHERE A.TRANSFORMER_ID = V_TRANSFORMER_ID
      AND A.STATUS = 0
      AND (A.CIRCUIT_ID IS NULL OR NVL(PC.IS_DELETED, 0) = 0)
      AND (A.POINT_ID IS NULL OR NVL(MP.IS_DELETED, 0) = 0);

    IF V_ACTIVE_COUNT = 0 THEN
      UPDATE BOX_TRANSFORMER
      SET STATUS = 0,
          UPDATED_AT = LOCALTIMESTAMP
      WHERE ID = V_TRANSFORMER_ID
        AND IS_DELETED = 0
        AND STATUS = 1;
    END IF;
  END IF;
END;
