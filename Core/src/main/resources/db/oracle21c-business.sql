CREATE OR REPLACE PACKAGE PKG_PSM_ARCHIVE AS
  PROCEDURE ARCHIVE_EXPIRED_RAW;
END PKG_PSM_ARCHIVE;
/

CREATE OR REPLACE PACKAGE BODY PKG_PSM_ARCHIVE AS
  PROCEDURE ARCHIVE_EXPIRED_RAW IS
    V_CUTOFF_DAY     DATE := TRUNC(CURRENT_DATE) - 30;
    V_MERGED_ROWS    NUMBER := 0;
    V_DELETED_RAW    NUMBER := 0;
    V_DELETED_LOGS   NUMBER := 0;
  BEGIN
    PKG_PSM_LOG.WRITE_LOG('INFO', 'ARCHIVE_START', 'Started raw data archive job', 'cutoffDay=' || TO_CHAR(V_CUTOFF_DAY, 'YYYY-MM-DD'));

    MERGE INTO TS_RAW_DATA_DAY_ARCHIVE DST
    USING (
      SELECT
        TRUNC(R.SAMPLE_TIME) AS ARCHIVE_DAY,
        R.TRANSFORMER_ID,
        R.CIRCUIT_ID,
        R.POINT_ID,
        CASE
          WHEN UPPER(MP.MEASURE_TYPE) IN ('ENERGY', 'SWITCH_STATUS', 'FUSE_STATUS', 'SMOKE_STATUS', 'DOOR_STATUS')
            THEN ROUND(MAX(R.VAL), 4)
          ELSE ROUND(AVG(R.VAL), 4)
        END AS DISPLAY_VAL,
        ROUND(AVG(R.VAL), 4) AS AVG_VAL,
        ROUND(MIN(R.VAL), 4) AS MIN_VAL,
        ROUND(MAX(R.VAL), 4) AS MAX_VAL,
        COUNT(*) AS SAMPLE_COUNT,
        MAX(R.QUALITY_FLAG) AS MAX_QUALITY_FLAG
      FROM TS_RAW_DATA R
      JOIN MEASURE_POINT MP ON MP.ID = R.POINT_ID
      WHERE R.SAMPLE_TIME < V_CUTOFF_DAY
      GROUP BY
        TRUNC(R.SAMPLE_TIME),
        R.TRANSFORMER_ID,
        R.CIRCUIT_ID,
        R.POINT_ID,
        UPPER(MP.MEASURE_TYPE)
    ) SRC
    ON (
      DST.ARCHIVE_DAY = SRC.ARCHIVE_DAY
      AND DST.POINT_ID = SRC.POINT_ID
    )
    WHEN MATCHED THEN UPDATE
    SET
      DST.TRANSFORMER_ID = SRC.TRANSFORMER_ID,
      DST.CIRCUIT_ID = SRC.CIRCUIT_ID,
      DST.DISPLAY_VAL = SRC.DISPLAY_VAL,
      DST.AVG_VAL = SRC.AVG_VAL,
      DST.MIN_VAL = SRC.MIN_VAL,
      DST.MAX_VAL = SRC.MAX_VAL,
      DST.SAMPLE_COUNT = SRC.SAMPLE_COUNT,
      DST.MAX_QUALITY_FLAG = SRC.MAX_QUALITY_FLAG,
      DST.CREATED_AT = LOCALTIMESTAMP
    WHEN NOT MATCHED THEN
      INSERT (
        ID,
        ARCHIVE_DAY,
        TRANSFORMER_ID,
        CIRCUIT_ID,
        POINT_ID,
        DISPLAY_VAL,
        AVG_VAL,
        MIN_VAL,
        MAX_VAL,
        SAMPLE_COUNT,
        MAX_QUALITY_FLAG,
        CREATED_AT
      )
      VALUES (
        SEQ_TS_RAW_DATA_DAY_ARCHIVE.NEXTVAL,
        SRC.ARCHIVE_DAY,
        SRC.TRANSFORMER_ID,
        SRC.CIRCUIT_ID,
        SRC.POINT_ID,
        SRC.DISPLAY_VAL,
        SRC.AVG_VAL,
        SRC.MIN_VAL,
        SRC.MAX_VAL,
        SRC.SAMPLE_COUNT,
        SRC.MAX_QUALITY_FLAG,
        LOCALTIMESTAMP
      );
    V_MERGED_ROWS := SQL%ROWCOUNT;

    DELETE FROM TS_RAW_DATA
    WHERE SAMPLE_TIME < V_CUTOFF_DAY;
    V_DELETED_RAW := SQL%ROWCOUNT;

    DELETE FROM DB_RUNTIME_LOG
    WHERE CREATED_AT < (LOCALTIMESTAMP - NUMTODSINTERVAL(30, 'DAY'));
    V_DELETED_LOGS := SQL%ROWCOUNT;

    COMMIT;
    PKG_PSM_LOG.WRITE_LOG(
      'INFO',
      'ARCHIVE_DONE',
      'Finished raw data archive job',
      'mergedRows=' || V_MERGED_ROWS || ' deletedRaw=' || V_DELETED_RAW || ' deletedLogs=' || V_DELETED_LOGS
    );
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      PKG_PSM_LOG.WRITE_LOG('ERROR', 'ARCHIVE_FAIL', SQLERRM, DBMS_UTILITY.FORMAT_ERROR_BACKTRACE);
      RAISE;
  END ARCHIVE_EXPIRED_RAW;
END PKG_PSM_ARCHIVE;
/

