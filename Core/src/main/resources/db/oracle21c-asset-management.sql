CREATE OR REPLACE PACKAGE BODY PKG_PSM_ASSET AS
  FUNCTION NORMALIZE_TEXT(P_TEXT IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN NULLIF(TRIM(P_TEXT), '');
  END NORMALIZE_TEXT;

  FUNCTION NORMALIZE_UPPER(P_TEXT IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN UPPER(NORMALIZE_TEXT(P_TEXT));
  END NORMALIZE_UPPER;

  PROCEDURE ASSERT_REQUIRED(P_VALUE IN VARCHAR2, P_LABEL IN VARCHAR2) IS
  BEGIN
    IF P_VALUE IS NULL THEN
      RAISE_APPLICATION_ERROR(-20500, P_LABEL || ' is required.');
    END IF;
  END ASSERT_REQUIRED;

  PROCEDURE ASSERT_TRANSFORMER_STATUS(P_STATUS IN NUMBER) IS
  BEGIN
    IF P_STATUS IS NULL OR P_STATUS NOT IN (0, 1, 2) THEN
      RAISE_APPLICATION_ERROR(-20501, 'Invalid transformer status.');
    END IF;
  END ASSERT_TRANSFORMER_STATUS;

  PROCEDURE ASSERT_CIRCUIT_STATUS(P_STATUS IN NUMBER) IS
  BEGIN
    IF P_STATUS IS NULL OR P_STATUS NOT IN (0, 1, 2) THEN
      RAISE_APPLICATION_ERROR(-20502, 'Invalid circuit status.');
    END IF;
  END ASSERT_CIRCUIT_STATUS;

  PROCEDURE ASSERT_POINT_STATUS(P_STATUS IN NUMBER) IS
  BEGIN
    IF P_STATUS IS NULL OR P_STATUS NOT IN (0, 1) THEN
      RAISE_APPLICATION_ERROR(-20503, 'Invalid measure point status.');
    END IF;
  END ASSERT_POINT_STATUS;

  PROCEDURE ASSERT_ACTIVE_TRANSFORMER(P_TRANSFORMER_ID IN NUMBER) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM BOX_TRANSFORMER
    WHERE ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0;

    IF V_COUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20504, 'Transformer does not exist.');
    END IF;
  END ASSERT_ACTIVE_TRANSFORMER;

  PROCEDURE ASSERT_ACTIVE_CIRCUIT(P_CIRCUIT_ID IN NUMBER, P_TRANSFORMER_ID IN NUMBER DEFAULT NULL) IS
    V_COUNT NUMBER;
  BEGIN
    IF P_CIRCUIT_ID IS NULL THEN
      RAISE_APPLICATION_ERROR(-20505, 'Circuit does not exist.');
    END IF;

    SELECT COUNT(1)
    INTO V_COUNT
    FROM POWER_CIRCUIT
    WHERE ID = P_CIRCUIT_ID
      AND IS_DELETED = 0
      AND (P_TRANSFORMER_ID IS NULL OR TRANSFORMER_ID = P_TRANSFORMER_ID);

    IF V_COUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20505, 'Circuit does not exist.');
    END IF;
  END ASSERT_ACTIVE_CIRCUIT;

  PROCEDURE ASSERT_ACTIVE_POINT(P_POINT_ID IN NUMBER) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM MEASURE_POINT
    WHERE ID = P_POINT_ID
      AND IS_DELETED = 0;

    IF V_COUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20506, 'Measure point does not exist.');
    END IF;
  END ASSERT_ACTIVE_POINT;

  PROCEDURE VALIDATE_DIRECTION(P_DIRECTION IN VARCHAR2) IS
  BEGIN
    IF P_DIRECTION IS NULL OR P_DIRECTION NOT IN ('INCOMING', 'OUTGOING') THEN
      RAISE_APPLICATION_ERROR(-20507, 'Invalid circuit direction.');
    END IF;
  END VALIDATE_DIRECTION;

  PROCEDURE VALIDATE_TRANSFORMER_UNIQUENESS(
      P_TRANSFORMER_ID IN NUMBER,
      P_TRANSFORMER_CODE IN VARCHAR2,
      P_NAME IN VARCHAR2
  ) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM BOX_TRANSFORMER
    WHERE IS_DELETED = 0
      AND ID != NVL(P_TRANSFORMER_ID, -1)
      AND UPPER(TRIM(TRANSFORMER_CODE)) = P_TRANSFORMER_CODE;

    IF V_COUNT > 0 THEN
      RAISE_APPLICATION_ERROR(-20508, 'Transformer code already exists.');
    END IF;

    SELECT COUNT(1)
    INTO V_COUNT
    FROM BOX_TRANSFORMER
    WHERE IS_DELETED = 0
      AND ID != NVL(P_TRANSFORMER_ID, -1)
      AND UPPER(TRIM(NAME)) = UPPER(TRIM(P_NAME));

    IF V_COUNT > 0 THEN
      RAISE_APPLICATION_ERROR(-20509, 'Transformer name already exists.');
    END IF;
  END VALIDATE_TRANSFORMER_UNIQUENESS;

  PROCEDURE VALIDATE_CIRCUIT_UNIQUENESS(
      P_CIRCUIT_ID IN NUMBER,
      P_TRANSFORMER_ID IN NUMBER,
      P_CIRCUIT_CODE IN VARCHAR2,
      P_NAME IN VARCHAR2,
      P_DIRECTION IN VARCHAR2
  ) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM POWER_CIRCUIT
    WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0
      AND ID != NVL(P_CIRCUIT_ID, -1)
      AND UPPER(TRIM(CIRCUIT_CODE)) = P_CIRCUIT_CODE;

    IF V_COUNT > 0 THEN
      RAISE_APPLICATION_ERROR(-20510, 'Circuit code already exists under this transformer.');
    END IF;

    SELECT COUNT(1)
    INTO V_COUNT
    FROM POWER_CIRCUIT
    WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0
      AND ID != NVL(P_CIRCUIT_ID, -1)
      AND UPPER(TRIM(NAME)) = UPPER(TRIM(P_NAME));

    IF V_COUNT > 0 THEN
      RAISE_APPLICATION_ERROR(-20511, 'Circuit name already exists under this transformer.');
    END IF;

    IF P_DIRECTION = 'INCOMING' THEN
      SELECT COUNT(1)
      INTO V_COUNT
      FROM POWER_CIRCUIT
      WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
        AND IS_DELETED = 0
        AND ID != NVL(P_CIRCUIT_ID, -1)
        AND DIRECTION = 'INCOMING';

      IF V_COUNT > 0 THEN
        RAISE_APPLICATION_ERROR(-20512, 'Only one active incoming circuit is allowed per transformer.');
      END IF;
    END IF;
  END VALIDATE_CIRCUIT_UNIQUENESS;

  PROCEDURE VALIDATE_POINT_UNIQUENESS(
      P_POINT_ID IN NUMBER,
      P_TRANSFORMER_ID IN NUMBER,
      P_POINT_CODE IN VARCHAR2,
      P_POINT_NAME IN VARCHAR2
  ) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM MEASURE_POINT
    WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0
      AND ID != NVL(P_POINT_ID, -1)
      AND UPPER(TRIM(POINT_CODE)) = P_POINT_CODE;

    IF V_COUNT > 0 THEN
      RAISE_APPLICATION_ERROR(-20513, 'Measure point code already exists under this transformer.');
    END IF;

    SELECT COUNT(1)
    INTO V_COUNT
    FROM MEASURE_POINT
    WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0
      AND ID != NVL(P_POINT_ID, -1)
      AND UPPER(TRIM(POINT_NAME)) = UPPER(TRIM(P_POINT_NAME));

    IF V_COUNT > 0 THEN
      RAISE_APPLICATION_ERROR(-20514, 'Measure point name already exists under this transformer.');
    END IF;
  END VALIDATE_POINT_UNIQUENESS;

  PROCEDURE VALIDATE_POINT_SCOPE(
      P_TRANSFORMER_ID IN NUMBER,
      P_CIRCUIT_ID IN NUMBER,
      P_POINT_GROUP IN VARCHAR2
  ) IS
    V_DIRECTION VARCHAR2(20);
  BEGIN
    IF P_CIRCUIT_ID IS NULL THEN
      IF P_POINT_GROUP NOT IN ('TRANSFORMER', 'CABINET') THEN
        RAISE_APPLICATION_ERROR(-20515, 'Transformer-level points must use TRANSFORMER or CABINET point groups.');
      END IF;
      RETURN;
    END IF;

    SELECT DIRECTION
    INTO V_DIRECTION
    FROM POWER_CIRCUIT
    WHERE ID = P_CIRCUIT_ID
      AND TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0;

    IF P_POINT_GROUP IN ('TRANSFORMER', 'CABINET') THEN
      RAISE_APPLICATION_ERROR(-20516, 'Transformer or cabinet points cannot be attached to a circuit.');
    END IF;

    IF V_DIRECTION = 'INCOMING' AND P_POINT_GROUP != 'ELECTRIC_IN' THEN
      RAISE_APPLICATION_ERROR(-20517, 'Incoming circuit points must use ELECTRIC_IN point group.');
    END IF;

    IF V_DIRECTION = 'OUTGOING' AND P_POINT_GROUP != 'ELECTRIC_OUT' THEN
      RAISE_APPLICATION_ERROR(-20518, 'Outgoing circuit points must use ELECTRIC_OUT point group.');
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RAISE_APPLICATION_ERROR(-20505, 'Circuit does not exist.');
  END VALIDATE_POINT_SCOPE;

  PROCEDURE CREATE_TRANSFORMER(
      P_TRANSFORMER_CODE   IN VARCHAR2,
      P_NAME               IN VARCHAR2,
      P_RATED_CAPACITY_KVA IN NUMBER,
      P_RATED_VOLTAGE_RATIO IN VARCHAR2,
      P_COMMISSION_DATE    IN DATE,
      P_MANUFACTURER       IN VARCHAR2,
      P_OIL_LEVEL          IN NUMBER,
      P_LOCATION           IN VARCHAR2,
      P_STATUS             IN NUMBER,
      P_TRANSFORMER_ID     OUT NUMBER
  ) IS
    V_CODE                  VARCHAR2(64) := NORMALIZE_UPPER(P_TRANSFORMER_CODE);
    V_NAME                  VARCHAR2(100) := NORMALIZE_TEXT(P_NAME);
    V_RATED_VOLTAGE_RATIO   VARCHAR2(50) := NORMALIZE_TEXT(P_RATED_VOLTAGE_RATIO);
    V_MANUFACTURER          VARCHAR2(100) := NORMALIZE_TEXT(P_MANUFACTURER);
    V_LOCATION              VARCHAR2(200) := NORMALIZE_TEXT(P_LOCATION);
    V_STATUS                NUMBER := NVL(P_STATUS, 0);
  BEGIN
    ASSERT_REQUIRED(V_CODE, 'Transformer code');
    ASSERT_REQUIRED(V_NAME, 'Transformer name');
    ASSERT_TRANSFORMER_STATUS(V_STATUS);
    VALIDATE_TRANSFORMER_UNIQUENESS(NULL, V_CODE, V_NAME);

    P_TRANSFORMER_ID := SEQ_BOX_TRANSFORMER.NEXTVAL;
    INSERT INTO BOX_TRANSFORMER (
      ID,
      TRANSFORMER_CODE,
      NAME,
      TRANSFORMER_TYPE,
      RATED_CAPACITY_KVA,
      RATED_VOLTAGE_RATIO,
      COMMISSION_DATE,
      MANUFACTURER,
      OIL_LEVEL,
      LOCATION,
      STATUS,
      IS_DELETED,
      CREATED_AT,
      UPDATED_AT
    ) VALUES (
      P_TRANSFORMER_ID,
      V_CODE,
      V_NAME,
      'BOX_TRANSFORMER',
      P_RATED_CAPACITY_KVA,
      V_RATED_VOLTAGE_RATIO,
      P_COMMISSION_DATE,
      V_MANUFACTURER,
      P_OIL_LEVEL,
      V_LOCATION,
      V_STATUS,
      0,
      LOCALTIMESTAMP,
      NULL
    );

    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      RAISE;
  END CREATE_TRANSFORMER;

  PROCEDURE UPDATE_TRANSFORMER(
      P_TRANSFORMER_ID     IN NUMBER,
      P_TRANSFORMER_CODE   IN VARCHAR2,
      P_NAME               IN VARCHAR2,
      P_RATED_CAPACITY_KVA IN NUMBER,
      P_RATED_VOLTAGE_RATIO IN VARCHAR2,
      P_COMMISSION_DATE    IN DATE,
      P_MANUFACTURER       IN VARCHAR2,
      P_OIL_LEVEL          IN NUMBER,
      P_LOCATION           IN VARCHAR2,
      P_STATUS             IN NUMBER
  ) IS
    V_CODE                  VARCHAR2(64) := NORMALIZE_UPPER(P_TRANSFORMER_CODE);
    V_NAME                  VARCHAR2(100) := NORMALIZE_TEXT(P_NAME);
    V_RATED_VOLTAGE_RATIO   VARCHAR2(50) := NORMALIZE_TEXT(P_RATED_VOLTAGE_RATIO);
    V_MANUFACTURER          VARCHAR2(100) := NORMALIZE_TEXT(P_MANUFACTURER);
    V_LOCATION              VARCHAR2(200) := NORMALIZE_TEXT(P_LOCATION);
  BEGIN
    ASSERT_ACTIVE_TRANSFORMER(P_TRANSFORMER_ID);
    ASSERT_REQUIRED(V_CODE, 'Transformer code');
    ASSERT_REQUIRED(V_NAME, 'Transformer name');
    ASSERT_TRANSFORMER_STATUS(P_STATUS);
    VALIDATE_TRANSFORMER_UNIQUENESS(P_TRANSFORMER_ID, V_CODE, V_NAME);

    UPDATE BOX_TRANSFORMER
    SET
      TRANSFORMER_CODE = V_CODE,
      NAME = V_NAME,
      RATED_CAPACITY_KVA = P_RATED_CAPACITY_KVA,
      RATED_VOLTAGE_RATIO = V_RATED_VOLTAGE_RATIO,
      COMMISSION_DATE = P_COMMISSION_DATE,
      MANUFACTURER = V_MANUFACTURER,
      OIL_LEVEL = P_OIL_LEVEL,
      LOCATION = V_LOCATION,
      STATUS = P_STATUS,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0;

    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      RAISE;
  END UPDATE_TRANSFORMER;

  PROCEDURE DELETE_TRANSFORMER(P_TRANSFORMER_ID IN NUMBER) IS
  BEGIN
    ASSERT_ACTIVE_TRANSFORMER(P_TRANSFORMER_ID);

    UPDATE MEASURE_POINT
    SET
      IS_DELETED = 1,
      STATUS = 1,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0;

    UPDATE POWER_CIRCUIT
    SET
      IS_DELETED = 1,
      STATUS = 2,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE TRANSFORMER_ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0;

    UPDATE BOX_TRANSFORMER
    SET
      IS_DELETED = 1,
      STATUS = 2,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = P_TRANSFORMER_ID
      AND IS_DELETED = 0;

    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      ROLLBACK;
      RAISE;
  END DELETE_TRANSFORMER;

  PROCEDURE CREATE_CIRCUIT(
      P_TRANSFORMER_ID   IN NUMBER,
      P_CIRCUIT_CODE     IN VARCHAR2,
      P_NAME             IN VARCHAR2,
      P_DIRECTION        IN VARCHAR2,
      P_RATED_VOLTAGE_KV IN NUMBER,
      P_RATED_CURRENT_A  IN NUMBER,
      P_STATUS           IN NUMBER,
      P_CIRCUIT_ID       OUT NUMBER
  ) IS
    V_CODE      VARCHAR2(64) := NORMALIZE_UPPER(P_CIRCUIT_CODE);
    V_NAME      VARCHAR2(100) := NORMALIZE_TEXT(P_NAME);
    V_DIRECTION VARCHAR2(20) := NORMALIZE_UPPER(P_DIRECTION);
    V_STATUS    NUMBER := NVL(P_STATUS, 0);
  BEGIN
    ASSERT_ACTIVE_TRANSFORMER(P_TRANSFORMER_ID);
    ASSERT_REQUIRED(V_CODE, 'Circuit code');
    ASSERT_REQUIRED(V_NAME, 'Circuit name');
    VALIDATE_DIRECTION(V_DIRECTION);
    ASSERT_CIRCUIT_STATUS(V_STATUS);
    VALIDATE_CIRCUIT_UNIQUENESS(NULL, P_TRANSFORMER_ID, V_CODE, V_NAME, V_DIRECTION);

    P_CIRCUIT_ID := SEQ_POWER_CIRCUIT.NEXTVAL;
    INSERT INTO POWER_CIRCUIT (
      ID,
      TRANSFORMER_ID,
      CIRCUIT_CODE,
      NAME,
      DIRECTION,
      RATED_VOLTAGE_KV,
      RATED_CURRENT_A,
      STATUS,
      IS_DELETED,
      CREATED_AT,
      UPDATED_AT
    ) VALUES (
      P_CIRCUIT_ID,
      P_TRANSFORMER_ID,
      V_CODE,
      V_NAME,
      V_DIRECTION,
