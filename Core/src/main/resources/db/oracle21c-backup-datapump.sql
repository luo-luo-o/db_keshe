  V_COUNT NUMBER;
BEGIN
  SELECT COUNT(1) INTO V_COUNT FROM USER_TABLES WHERE TABLE_NAME = 'DATA_SNAPSHOT';
  IF V_COUNT = 0 THEN
    EXECUTE IMMEDIATE q'[
      CREATE TABLE DATA_SNAPSHOT (
        ID NUMBER(19) NOT NULL,
        SNAPSHOT_NAME VARCHAR2(100) NOT NULL,
        NOTE VARCHAR2(500),
        DUMP_FILE_NAME VARCHAR2(255),
        LOG_FILE_NAME VARCHAR2(255),
        RESTORE_LOG_FILE_NAME VARCHAR2(255),
        STATUS VARCHAR2(30) DEFAULT 'FAILED' NOT NULL,
        TRANSFORMER_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        CIRCUIT_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        POINT_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        RAW_DATA_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        ARCHIVE_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        DOOR_LOG_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        ALARM_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        TASK_COUNT NUMBER(19) DEFAULT 0 NOT NULL,
        CREATED_BY VARCHAR2(64),
        CREATED_AT TIMESTAMP DEFAULT LOCALTIMESTAMP NOT NULL,
        RESTORED_BY VARCHAR2(64),
        RESTORED_AT TIMESTAMP,
        ERROR_MESSAGE VARCHAR2(2000),
        CONSTRAINT PK_DATA_SNAPSHOT PRIMARY KEY (ID)
      )
    ]';
  END IF;
END;
/

DECLARE
  PROCEDURE ADD_COLUMN(P_COLUMN_NAME IN VARCHAR2, P_DEFINITION IN VARCHAR2) IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM USER_TAB_COLS
    WHERE TABLE_NAME = 'DATA_SNAPSHOT'
      AND COLUMN_NAME = P_COLUMN_NAME;

    IF V_COUNT = 0 THEN
      EXECUTE IMMEDIATE 'ALTER TABLE DATA_SNAPSHOT ADD (' || P_DEFINITION || ')';
    END IF;
  END ADD_COLUMN;
BEGIN
  ADD_COLUMN('DUMP_FILE_NAME', 'DUMP_FILE_NAME VARCHAR2(255)');
  ADD_COLUMN('LOG_FILE_NAME', 'LOG_FILE_NAME VARCHAR2(255)');
  ADD_COLUMN('RESTORE_LOG_FILE_NAME', 'RESTORE_LOG_FILE_NAME VARCHAR2(255)');
  ADD_COLUMN('STATUS', 'STATUS VARCHAR2(30) DEFAULT ''FAILED'' NOT NULL');
  ADD_COLUMN('ARCHIVE_COUNT', 'ARCHIVE_COUNT NUMBER(19) DEFAULT 0 NOT NULL');
  ADD_COLUMN('DOOR_LOG_COUNT', 'DOOR_LOG_COUNT NUMBER(19) DEFAULT 0 NOT NULL');
  ADD_COLUMN('ERROR_MESSAGE', 'ERROR_MESSAGE VARCHAR2(2000)');
END;
/

UPDATE DATA_SNAPSHOT
SET ERROR_MESSAGE = NVL(ERROR_MESSAGE, 'Legacy in-schema snapshot cannot be restored by Data Pump.')
WHERE DUMP_FILE_NAME IS NULL
  AND STATUS = 'FAILED'
  AND ERROR_MESSAGE IS NULL;

COMMIT;

