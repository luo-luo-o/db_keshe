CREATE OR REPLACE TRIGGER TRG_PSM_ALARM_OPEN_STATUS
AFTER INSERT ON ALARM_LOG
FOR EACH ROW
BEGIN
  IF :NEW.STATUS = 0 AND UPPER(NVL(:NEW.ALARM_LEVEL, '')) = 'SERIOUS' THEN
    UPDATE BOX_TRANSFORMER
    SET STATUS = 1,
        UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = :NEW.TRANSFORMER_ID
      AND IS_DELETED = 0
      AND STATUS = 0;

    IF :NEW.CIRCUIT_ID IS NOT NULL THEN
      UPDATE POWER_CIRCUIT
      SET STATUS = 1,
          UPDATED_AT = LOCALTIMESTAMP
      WHERE ID = :NEW.CIRCUIT_ID
        AND IS_DELETED = 0
        AND STATUS = 0;
    END IF;
  END IF;
END;
