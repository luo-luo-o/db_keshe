CREATE SEQUENCE SEQ_ALARM_LOG START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_MAINT_TASK START WITH 1 INCREMENT BY 1 NOCACHE;

