CREATE OR REPLACE PACKAGE BODY PKG_PSM_SIM AS
  C_NORMAL_INTERVAL_MS CONSTANT NUMBER := 1000;
  C_ANOMALY_INTERVAL_MS CONSTANT NUMBER := 100;

  FUNCTION HAS_MEASURE_POINT_SOFT_DELETE RETURN NUMBER IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1)
    INTO V_COUNT
    FROM USER_TAB_COLS
    WHERE TABLE_NAME = 'MEASURE_POINT'
      AND COLUMN_NAME = 'IS_DELETED';

    RETURN CASE WHEN V_COUNT > 0 THEN 1 ELSE 0 END;
  END HAS_MEASURE_POINT_SOFT_DELETE;

  PROCEDURE ENSURE_CONTROL_ROW IS
    V_COUNT NUMBER;
  BEGIN
    SELECT COUNT(1) INTO V_COUNT FROM SIM_CONTROL WHERE ID = 1;
    IF V_COUNT = 0 THEN
      INSERT INTO SIM_CONTROL (
        ID,
        RUNNING,
        ANOMALY_ENABLED,
        WRITE_COUNT,
        ALARM_COUNT,
        TASK_COUNT,
        NORMAL_INTERVAL_MS,
        ANOMALY_INTERVAL_MS,
        UPDATED_AT
      )
      VALUES (
        1,
        0,
        0,
        0,
        0,
        0,
        C_NORMAL_INTERVAL_MS,
        C_ANOMALY_INTERVAL_MS,
        LOCALTIMESTAMP
      );
    END IF;
  END ENSURE_CONTROL_ROW;

  FUNCTION NEXT_VALUE(
      P_MEASURE_TYPE IN VARCHAR2,
      P_UNIT         IN VARCHAR2,
      P_MAX_LIMIT    IN NUMBER,
      P_ANOMALY      IN NUMBER,
      P_WRITE_COUNT  IN NUMBER
  ) RETURN NUMBER IS
    V_TYPE  VARCHAR2(50) := UPPER(NVL(P_MEASURE_TYPE, ''));
    V_UNIT  VARCHAR2(20) := UPPER(NVL(P_UNIT, ''));
    V_BASE  NUMBER := 0;
    V_DRIFT NUMBER := 0;
  BEGIN
    CASE V_TYPE
      WHEN 'VOLTAGE' THEN
        IF V_UNIT = 'V' THEN
          V_BASE := CASE WHEN P_ANOMALY = 1 THEN 445 ELSE 400 END;
        ELSE
          V_BASE := CASE WHEN P_ANOMALY = 1 THEN 11.2 ELSE 10.1 END;
        END IF;
      WHEN 'CURRENT' THEN
        V_BASE := NVL(P_MAX_LIMIT, 100) * CASE WHEN P_ANOMALY = 1 THEN 1.08 ELSE 0.62 END;
      WHEN 'POWER_FACTOR' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 0.7800 ELSE 0.9300 END;
      WHEN 'ENERGY' THEN
        V_BASE := 12000 + (NVL(P_WRITE_COUNT, 0) * 0.0200);
      WHEN 'FREQUENCY' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 49.2000 ELSE 50.0200 END;
      WHEN 'OIL_TEMP' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 91.0000 ELSE 63.0000 END;
      WHEN 'CABINET_TEMP' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 50.0000 ELSE 32.0000 END;
      WHEN 'CABINET_HUMIDITY' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 88.0000 ELSE 55.0000 END;
      WHEN 'SWITCH_STATUS' THEN
        V_BASE := 1;
      WHEN 'FUSE_STATUS' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 1 ELSE 0 END;
      WHEN 'SMOKE_STATUS' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 1 ELSE 0 END;
      WHEN 'DOOR_STATUS' THEN
        V_BASE := CASE WHEN P_ANOMALY = 1 THEN 1 ELSE 0 END;
      ELSE
        V_BASE := 0;
    END CASE;

    IF V_TYPE NOT IN ('SWITCH_STATUS', 'FUSE_STATUS', 'SMOKE_STATUS', 'DOOR_STATUS', 'POWER_FACTOR') THEN
      V_DRIFT := (MOD(NVL(P_WRITE_COUNT, 0), 7) - 3) * 0.0500;
    END IF;

    RETURN ROUND(V_BASE + V_DRIFT, 4);
  END NEXT_VALUE;

  FUNCTION IS_OUT_OF_RANGE(
      P_MIN_LIMIT IN NUMBER,
      P_MAX_LIMIT IN NUMBER,
      P_VALUE     IN NUMBER
  ) RETURN NUMBER IS
  BEGIN
    IF P_MIN_LIMIT IS NOT NULL AND P_VALUE < P_MIN_LIMIT THEN
      RETURN 1;
    END IF;

    IF P_MAX_LIMIT IS NOT NULL AND P_VALUE > P_MAX_LIMIT THEN
      RETURN 1;
    END IF;

    RETURN 0;
  END IS_OUT_OF_RANGE;

  FUNCTION ALARM_TYPE_FOR(
      P_MEASURE_TYPE IN VARCHAR2,
      P_MIN_LIMIT    IN NUMBER,
      P_VALUE        IN NUMBER
  ) RETURN VARCHAR2 IS
  BEGIN
    IF UPPER(NVL(P_MEASURE_TYPE, '')) = 'POWER_FACTOR'
       AND P_MIN_LIMIT IS NOT NULL
       AND P_VALUE < P_MIN_LIMIT THEN
      RETURN 'POWER_FACTOR_LOW';
    END IF;

    RETURN 'RANGE_LIMIT';
  END ALARM_TYPE_FOR;

  PROCEDURE OPEN_TASK_FOR_ALARM(P_ALARM_ID IN NUMBER) IS
  BEGIN
    INSERT INTO MAINT_TASK (
      TASK_ID,
      ALARM_ID,
      STATUS,
      ASSIGNEE,
      FEEDBACK,
      CREATED_AT,
      UPDATED_AT,
      FINISHED_AT
    )
    VALUES (
      SEQ_MAINT_TASK.NEXTVAL,
      P_ALARM_ID,
      0,
      'engineer01',
      NULL,
      LOCALTIMESTAMP,
      NULL,
      NULL
    );
  END OPEN_TASK_FOR_ALARM;

  PROCEDURE HANDLE_POINT(
      P_TRANSFORMER_ID IN NUMBER,
      P_CIRCUIT_ID     IN NUMBER,
      P_POINT_ID       IN NUMBER,
      P_POINT_CODE     IN VARCHAR2,
      P_POINT_NAME     IN VARCHAR2,
      P_MEASURE_TYPE   IN VARCHAR2,
      P_UNIT           IN VARCHAR2,
      P_MIN_LIMIT      IN NUMBER,
      P_MAX_LIMIT      IN NUMBER,
      P_SAMPLE_TIME    IN TIMESTAMP,
      P_ANOMALY        IN NUMBER,
      P_WRITE_COUNT    IN NUMBER,
      P_ALARM_COUNT    IN OUT NUMBER,
      P_TASK_COUNT     IN OUT NUMBER
  ) IS
    V_VALUE           NUMBER;
    V_QUALITY_FLAG    NUMBER;
    V_ALARM_ID        NUMBER;
    V_ACTIVE_ALARM_ID NUMBER;
    V_ALARM_TYPE      VARCHAR2(50);
  BEGIN
    V_VALUE := NEXT_VALUE(P_MEASURE_TYPE, P_UNIT, P_MAX_LIMIT, P_ANOMALY, P_WRITE_COUNT);
    V_QUALITY_FLAG := IS_OUT_OF_RANGE(P_MIN_LIMIT, P_MAX_LIMIT, V_VALUE);
    V_ALARM_TYPE := ALARM_TYPE_FOR(P_MEASURE_TYPE, P_MIN_LIMIT, V_VALUE);

    INSERT INTO TS_RAW_DATA (
      ID,
      TRANSFORMER_ID,
      CIRCUIT_ID,
      POINT_ID,
      SAMPLE_TIME,
      VAL,
      QUALITY_FLAG,
      CREATED_AT
    )
    VALUES (
      SEQ_TS_RAW_DATA.NEXTVAL,
      P_TRANSFORMER_ID,
      P_CIRCUIT_ID,
      P_POINT_ID,
      P_SAMPLE_TIME,
      V_VALUE,
      V_QUALITY_FLAG,
      LOCALTIMESTAMP
    );

    BEGIN
      SELECT ID
      INTO V_ACTIVE_ALARM_ID
      FROM (
        SELECT ID
        FROM ALARM_LOG
        WHERE POINT_ID = P_POINT_ID
          AND STATUS = 0
        ORDER BY ID DESC
      )
      WHERE ROWNUM = 1;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        V_ACTIVE_ALARM_ID := NULL;
    END;

    IF V_QUALITY_FLAG = 1 THEN
      IF V_ACTIVE_ALARM_ID IS NULL THEN
        V_ALARM_ID := SEQ_ALARM_LOG.NEXTVAL;
        INSERT INTO ALARM_LOG (
          ID,
          TRANSFORMER_ID,
          CIRCUIT_ID,
          POINT_ID,
          ALARM_TYPE,
          ALARM_LEVEL,
          START_TIME,
          END_TIME,
          START_VAL,
          END_VAL,
          STATUS,
          CREATED_AT
        )
        VALUES (
          V_ALARM_ID,
          P_TRANSFORMER_ID,
          P_CIRCUIT_ID,
          P_POINT_ID,
          V_ALARM_TYPE,
          'SERIOUS',
          P_SAMPLE_TIME,
          NULL,
          V_VALUE,
          V_VALUE,
          0,
          LOCALTIMESTAMP
        );

        OPEN_TASK_FOR_ALARM(V_ALARM_ID);
        P_ALARM_COUNT := P_ALARM_COUNT + 1;
        P_TASK_COUNT := P_TASK_COUNT + 1;
        PKG_PSM_LOG.WRITE_LOG(
          'WARN',
          'ALARM_OPEN',
          'Opened active alarm for point ' || P_POINT_CODE,
          'pointId=' || P_POINT_ID || ' pointName=' || P_POINT_NAME || ' value=' || TO_CHAR(V_VALUE)
        );
        PKG_PSM_LOG.WRITE_LOG(
          'INFO',
          'TASK_CREATE',
          'Created maintenance task for point ' || P_POINT_CODE,
          'pointId=' || P_POINT_ID || ' alarmId=' || V_ALARM_ID
        );
      ELSE
        UPDATE ALARM_LOG
        SET END_VAL = V_VALUE
        WHERE ID = V_ACTIVE_ALARM_ID;
      END IF;
    ELSIF V_ACTIVE_ALARM_ID IS NOT NULL THEN
      UPDATE ALARM_LOG
      SET
        STATUS = 1,
        END_TIME = P_SAMPLE_TIME,
        END_VAL = V_VALUE
      WHERE ID = V_ACTIVE_ALARM_ID;

      PKG_PSM_LOG.WRITE_LOG(
        'INFO',
        'ALARM_CLOSE',
        'Closed active alarm for point ' || P_POINT_CODE,
        'pointId=' || P_POINT_ID || ' pointName=' || P_POINT_NAME || ' value=' || TO_CHAR(V_VALUE)
      );
    END IF;
  END HANDLE_POINT;

  PROCEDURE START_RUN IS
  BEGIN
    ENSURE_CONTROL_ROW;
    UPDATE SIM_CONTROL
    SET
      RUNNING = 1,
      ANOMALY_ENABLED = 0,
      STARTED_AT = LOCALTIMESTAMP,
      LAST_WRITE_AT = NULL,
      WRITE_COUNT = 0,
      ALARM_COUNT = 0,
      TASK_COUNT = 0,
      NORMAL_INTERVAL_MS = C_NORMAL_INTERVAL_MS,
      ANOMALY_INTERVAL_MS = C_ANOMALY_INTERVAL_MS,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = 1;
    COMMIT;
    PKG_PSM_LOG.WRITE_LOG('INFO', 'SIM_START', 'Simulation started', 'normalIntervalMs=1000 anomalyIntervalMs=100');
  END START_RUN;

  PROCEDURE STOP_RUN IS
  BEGIN
    ENSURE_CONTROL_ROW;
    UPDATE SIM_CONTROL
    SET
      RUNNING = 0,
      ANOMALY_ENABLED = 0,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = 1;
    COMMIT;
    PKG_PSM_LOG.WRITE_LOG('INFO', 'SIM_STOP', 'Simulation stopped', NULL);
  END STOP_RUN;

  PROCEDURE SET_ANOMALY(P_ENABLED IN NUMBER) IS
  BEGIN
    ENSURE_CONTROL_ROW;
    UPDATE SIM_CONTROL
    SET
      ANOMALY_ENABLED = CASE WHEN NVL(P_ENABLED, 0) = 0 THEN 0 ELSE 1 END,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = 1;
    COMMIT;
    PKG_PSM_LOG.WRITE_LOG(
      'INFO',
      'SIM_MODE',
      CASE WHEN NVL(P_ENABLED, 0) = 0 THEN 'Simulation switched to normal mode' ELSE 'Simulation switched to anomaly mode' END,
      CASE WHEN NVL(P_ENABLED, 0) = 0 THEN 'currentIntervalMs=1000' ELSE 'currentIntervalMs=100' END
    );
  END SET_ANOMALY;

  PROCEDURE TICK IS
    V_RUNNING             NUMBER;
    V_ANOMALY_ENABLED     NUMBER;
    V_LAST_WRITE_AT       TIMESTAMP;
    V_WRITE_COUNT         NUMBER;
    V_ALARM_COUNT         NUMBER;
    V_TASK_COUNT          NUMBER;
    V_NORMAL_INTERVAL_MS  NUMBER;
    V_ANOMALY_INTERVAL_MS NUMBER;
    V_CURRENT_INTERVAL_MS NUMBER;
    V_SAMPLE_TIME         TIMESTAMP := LOCALTIMESTAMP;
    V_HAS_SOFT_DELETE     NUMBER;
    V_POINT_CURSOR        SYS_REFCURSOR;
    V_POINT_ID            NUMBER;
    V_TRANSFORMER_ID      NUMBER;
    V_CIRCUIT_ID          NUMBER;
    V_POINT_CODE          VARCHAR2(64);
    V_POINT_NAME          VARCHAR2(100);
    V_MEASURE_TYPE        VARCHAR2(50);
    V_UNIT                VARCHAR2(20);
    V_MIN_LIMIT           NUMBER;
    V_MAX_LIMIT           NUMBER;
  BEGIN
    ENSURE_CONTROL_ROW;

    SELECT
      RUNNING,
      ANOMALY_ENABLED,
      LAST_WRITE_AT,
      WRITE_COUNT,
      ALARM_COUNT,
      TASK_COUNT,
      NORMAL_INTERVAL_MS,
      ANOMALY_INTERVAL_MS
    INTO
      V_RUNNING,
      V_ANOMALY_ENABLED,
      V_LAST_WRITE_AT,
      V_WRITE_COUNT,
      V_ALARM_COUNT,
      V_TASK_COUNT,
      V_NORMAL_INTERVAL_MS,
      V_ANOMALY_INTERVAL_MS
    FROM SIM_CONTROL
    WHERE ID = 1
    FOR UPDATE;

    IF V_RUNNING = 0 THEN
      COMMIT;
      RETURN;
    END IF;

    V_CURRENT_INTERVAL_MS := CASE WHEN V_ANOMALY_ENABLED = 1 THEN V_ANOMALY_INTERVAL_MS ELSE V_NORMAL_INTERVAL_MS END;

    IF V_LAST_WRITE_AT IS NOT NULL
       AND V_SAMPLE_TIME < (V_LAST_WRITE_AT + NUMTODSINTERVAL(V_CURRENT_INTERVAL_MS / 1000, 'SECOND')) THEN
      COMMIT;
      RETURN;
    END IF;

    V_HAS_SOFT_DELETE := HAS_MEASURE_POINT_SOFT_DELETE;

    IF V_HAS_SOFT_DELETE = 1 THEN
      OPEN V_POINT_CURSOR FOR
        'SELECT
           ID,
           TRANSFORMER_ID,
           CIRCUIT_ID,
           POINT_CODE,
           POINT_NAME,
           MEASURE_TYPE,
           UNIT,
           MIN_LIMIT,
           MAX_LIMIT
         FROM MEASURE_POINT
         WHERE STATUS = 0
           AND NVL(IS_DELETED, 0) = 0
         ORDER BY TRANSFORMER_ID, CIRCUIT_ID NULLS LAST, ID';
    ELSE
      OPEN V_POINT_CURSOR FOR
        'SELECT
           ID,
           TRANSFORMER_ID,
           CIRCUIT_ID,
           POINT_CODE,
           POINT_NAME,
           MEASURE_TYPE,
           UNIT,
           MIN_LIMIT,
           MAX_LIMIT
         FROM MEASURE_POINT
         WHERE STATUS = 0
         ORDER BY TRANSFORMER_ID, CIRCUIT_ID NULLS LAST, ID';
    END IF;

    LOOP
      FETCH V_POINT_CURSOR INTO
        V_POINT_ID,
        V_TRANSFORMER_ID,
        V_CIRCUIT_ID,
        V_POINT_CODE,
        V_POINT_NAME,
        V_MEASURE_TYPE,
        V_UNIT,
        V_MIN_LIMIT,
        V_MAX_LIMIT;
      EXIT WHEN V_POINT_CURSOR%NOTFOUND;

      V_WRITE_COUNT := V_WRITE_COUNT + 1;
      HANDLE_POINT(
        V_TRANSFORMER_ID,
        V_CIRCUIT_ID,
        V_POINT_ID,
        V_POINT_CODE,
        V_POINT_NAME,
        V_MEASURE_TYPE,
        V_UNIT,
        V_MIN_LIMIT,
        V_MAX_LIMIT,
        V_SAMPLE_TIME,
        V_ANOMALY_ENABLED,
        V_WRITE_COUNT,
        V_ALARM_COUNT,
        V_TASK_COUNT
      );
    END LOOP;

    CLOSE V_POINT_CURSOR;

    UPDATE SIM_CONTROL
    SET
      LAST_WRITE_AT = V_SAMPLE_TIME,
      WRITE_COUNT = V_WRITE_COUNT,
      ALARM_COUNT = V_ALARM_COUNT,
      TASK_COUNT = V_TASK_COUNT,
      UPDATED_AT = LOCALTIMESTAMP
    WHERE ID = 1;

    COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
      IF V_POINT_CURSOR%ISOPEN THEN
        CLOSE V_POINT_CURSOR;
      END IF;
      ROLLBACK;
      PKG_PSM_LOG.WRITE_LOG('ERROR', 'SIM_TICK', SQLERRM, DBMS_UTILITY.FORMAT_ERROR_BACKTRACE);
      RAISE;
  END TICK;

  PROCEDURE GET_STATUS(P_CURSOR OUT SYS_REFCURSOR) IS
  BEGIN
    ENSURE_CONTROL_ROW;
    OPEN P_CURSOR FOR
      SELECT
        RUNNING,
        ANOMALY_ENABLED,
        STARTED_AT,
        LAST_WRITE_AT,
        WRITE_COUNT,
        ALARM_COUNT,
        TASK_COUNT,
        NORMAL_INTERVAL_MS,
        ANOMALY_INTERVAL_MS,
        CASE WHEN ANOMALY_ENABLED = 1 THEN ANOMALY_INTERVAL_MS ELSE NORMAL_INTERVAL_MS END AS CURRENT_INTERVAL_MS
      FROM SIM_CONTROL
      WHERE ID = 1;
  END GET_STATUS;
END PKG_PSM_SIM;
